CREATE OR REPLACE TRIGGER TRG_LOG_EQP
AFTER INSERT OR DELETE OR UPDATE OF VALOR_EQPTO ON EQUIPAMENTO
FOR EACH ROW
BEGIN
 IF INSERTING THEN INSERT INTO LOG_ALTERACAO_EQPTO VALUES (:NEW.COD_EQPTO, SYSDATE, LOCALTIMESTAMP, 'INSERÇÃO', USER, NULL, :NEW.VALOR_EQPTO);

ELSIF UPDATING THEN INSERT INTO LOG_ALTERACAO_EQPTO VALUES (:NEW.COD_EQPTO, SYSDATE, LOCALTIMESTAMP, 'ATUALIZAÇÃO', USER, :OLD.VALOR_EQPTO, :NEW.VALOR_EQPTO);

ELSIF DELETING THEN INSERT INTO LOG_ALTERACAO_EQPTO VALUES (:NEW.COD_EQPTO, SYSDATE, LOCALTIMESTAMP, 'EXCLUSÃO', USER, :OLD.VALOR_EQPTO, 'EQUIPAMENTO EXCLUÍDO');

END IF;
END;

