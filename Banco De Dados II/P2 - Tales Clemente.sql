3)Padrão ANSI:
SELECT O.NOME AS OPERADOR, NVL(TO_CHAR(MONTHS_BETWEEN(OP.DATA_FIM, OP.DATA_INICIO)), 'NÃO REALIZOU OPERAÇÕES') AS MESES_DE_OPERACAO
FROM OPERADOR O
LEFT JOIN OPERACAO OP
ON O.CPF = OP.CPF AND OP.DATA_FIM > '01/01/2010'

Padrão SGBD:
SELECT O.NOME AS OPERADOR, NVL(TO_CHAR(MONTHS_BETWEEN(OP.DATA_FIM, OP.DATA_INICIO)), 'NÃO REALIZOU OPERAÇÕES') AS MESES_DE_OPERACAO
FROM   OPERADOR O, OPERACAO OP
WHERE  O.CPF = OP.CPF (+)
AND    OP.DATA_FIM (+) > '01/01/2010'


4)SELECT UPPER(TE.NOME_TIPO_EQPTO) AS NOME_TIPO_EQUIPAMENTO, 'R$' ||SUM(E.VALOR_EQPTO) AS VALOR_TOTAL_TIPO
FROM TIPO_EQUIPAMENTO TE
INNER JOIN EQUIPAMENTO E
ON E.CODIGO_TIPO_EQTO = TE.CODIGO_TIPO_EQTO
HAVING SUM(E.VALOR_EQPTO) > 500000
GROUP BY TE.NOME_TIPO_EQPTO
ORDER BY TE.NOME_TIPO_EQPTO

